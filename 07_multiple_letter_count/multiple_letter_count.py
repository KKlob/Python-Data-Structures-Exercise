def multiple_letter_count(phrase):
    """Return dict of {ltr: frequency} from phrase.

        >>> multiple_letter_count('yay')
        {'y': 2, 'a': 1}

        >>> multiple_letter_count('Yay')
        {'Y': 1, 'a': 1, 'y': 1}
    """

    phrase.lower()
    letter_dic = {}

    for letter in phrase:
        letter_dic[letter] = phrase.count(letter)

    return letter_dic